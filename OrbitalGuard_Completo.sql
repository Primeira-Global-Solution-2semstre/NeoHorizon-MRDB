-- =====================================================
-- 5. FUNÇÕES (FUNCTIONS)
-- =====================================================
CREATE OR REPLACE FUNCTION FN_TOTAL_ALERTAS RETURN NUMBER IS
 V_TOTAL NUMBER;
BEGIN
 SELECT COUNT(*) INTO V_TOTAL FROM ALERTAS;
 RETURN V_TOTAL;
END;
/

CREATE OR REPLACE FUNCTION FN_CLIENTE_PREMIUM(P_ID NUMBER) RETURN VARCHAR2 IS
 V_PLANO VARCHAR2(20);
BEGIN
 SELECT PLANO INTO V_PLANO FROM CLIENTES WHERE ID_CLIENTE = P_ID;
 IF V_PLANO = 'PREMIUM' THEN 
    RETURN 'SIM'; 
 ELSE 
    RETURN 'NAO'; 
 END IF;
EXCEPTION
 WHEN NO_DATA_FOUND THEN
    RETURN 'INEXISTENTE';
END;
/

CREATE OR REPLACE FUNCTION FN_CALCULAR_RISCO(P_PROB NUMBER) RETURN VARCHAR2 IS
BEGIN
 IF P_PROB < 30 THEN 
    RETURN 'BAIXO';
 ELSIF P_PROB < 70 THEN 
    RETURN 'MEDIO';
 ELSE 
    RETURN 'ALTO';
 END IF;
END;
/

-- =====================================================
-- 6. PROCEDIMENTOS (PROCEDURES)
-- =====================================================
CREATE OR REPLACE PROCEDURE PRC_GERAR_ALERTA(P_DETRITO NUMBER, P_RISCO NUMBER) IS
BEGIN
 INSERT INTO ALERTAS VALUES(SEQ_ALERTAS.NEXTVAL, P_DETRITO, P_RISCO, 'ATIVO');
END;
/

CREATE OR REPLACE PROCEDURE PRC_REGISTRAR_CONSULTA(P_CLIENTE NUMBER, P_ENDPOINT VARCHAR2) IS
BEGIN
 INSERT INTO CONSULTAS_API VALUES(SEQ_CONSULTAS.NEXTVAL, P_CLIENTE, P_ENDPOINT, SYSDATE);
END;
/

CREATE OR REPLACE PROCEDURE PRC_ATUALIZAR_ORBITA(P_ID NUMBER, P_ALT NUMBER) IS
BEGIN
 UPDATE ORBITAS SET ALTITUDE = P_ALT WHERE ID_DETRITO = P_ID;
END;
/

-- =====================================================
-- 7. GATILHOS (TRIGGERS)
-- =====================================================
CREATE OR REPLACE TRIGGER TRG_LOG_CONSULTA
AFTER INSERT ON CONSULTAS_API
FOR EACH ROW
BEGIN
 DBMS_OUTPUT.PUT_LINE('LOG NeoHorizon: Endpoint ' || :NEW.ENDPOINT || ' acessado.');
END;
/

CREATE OR REPLACE TRIGGER TRG_ALERTA_COLISAO
AFTER INSERT ON COLISOES_PREVISTAS
FOR EACH ROW
BEGIN
 IF :NEW.PROBABILIDADE > 70 THEN
   INSERT INTO ALERTAS VALUES(SEQ_ALERTAS.NEXTVAL, :NEW.ID_DETRITO, :NEW.PROBABILIDADE, 'CRITICO');
 END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_STATUS_DETRITO
BEFORE UPDATE ON DETRITOS
FOR EACH ROW
BEGIN
 :NEW.STATUS := UPPER(:NEW.STATUS);
END;
/

-- =====================================================
-- 8. PACOTES (PACKAGES)
-- =====================================================
CREATE OR REPLACE PACKAGE PKG_NEOHORIZON AS
 FUNCTION TOTAL_ALERTAS RETURN NUMBER;
 PROCEDURE GERAR_ALERTA(P_DETRITO NUMBER, P_RISCO NUMBER);
END;
/

CREATE OR REPLACE PACKAGE BODY PKG_NEOHORIZON AS
 FUNCTION TOTAL_ALERTAS RETURN NUMBER IS
 BEGIN
  RETURN FN_TOTAL_ALERTAS;
 END;
 PROCEDURE GERAR_ALERTA(P_DETRITO NUMBER, P_RISCO NUMBER) IS
 BEGIN
  PRC_GERAR_ALERTA(P_DETRITO, P_RISCO);
 END;
END;
/

