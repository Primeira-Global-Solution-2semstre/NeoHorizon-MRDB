-- Bloco 1
DECLARE 
 V_QTD NUMBER;
BEGIN
 SELECT COUNT(*) INTO V_QTD FROM DETRITOS;
 IF V_QTD = 0 THEN
    RAISE NO_DATA_FOUND;
 END IF;
 DBMS_OUTPUT.PUT_LINE('Bloco 1 - Total de Detritos Cadastrados: ' || V_QTD);
EXCEPTION 
 WHEN NO_DATA_FOUND THEN 
    DBMS_OUTPUT.PUT_LINE('Bloco 1 - Erro: Nenhum detrito encontrado.');
 WHEN OTHERS THEN 
    DBMS_OUTPUT.PUT_LINE('Bloco 1 - Erro inesperado.');
END;
/

-- Bloco 2
DECLARE 
 V_STATUS VARCHAR2(20);
BEGIN
 SELECT STATUS INTO V_STATUS FROM DETRITOS WHERE ID_DETRITO = 999;
 DBMS_OUTPUT.PUT_LINE('Bloco 2 - Status: ' || V_STATUS);
EXCEPTION 
 WHEN NO_DATA_FOUND THEN 
    DBMS_OUTPUT.PUT_LINE('Bloco 2 - Sucesso no Tratamento: Detrito 999 não existe.');
END;
/

-- Bloco 3
DECLARE 
 V_RISCO NUMBER := 85;
 V_GRAVIDADE VARCHAR2(30);
BEGIN
 IF V_RISCO < 30 THEN 
    V_GRAVIDADE := 'ESTÁVEL / BAIXO';
 ELSIF V_RISCO BETWEEN 30 AND 70 THEN 
    V_GRAVIDADE := 'ATENÇÃO / MÉDIO';
 ELSE 
    V_GRAVIDADE := 'CRÍTICO / ALTO';
 END IF;
 DBMS_OUTPUT.PUT_LINE('Bloco 3 - Nível de Risco Simulado: ' || V_GRAVIDADE);
 
 IF V_RISCO > 0 THEN
    V_RISCO := V_RISCO / 0;
 END IF;
EXCEPTION 
 WHEN ZERO_DIVIDE THEN 
    DBMS_OUTPUT.PUT_LINE('Bloco 3 - Exceção Tratada: Divisão por zero monitorada.');
END;
/

-- Bloco 4
DECLARE 
 V_CONTADOR NUMBER := 1;
 V_NOME_DET VARCHAR2(100);
BEGIN
 WHILE V_CONTADOR <= 5 LOOP
   SELECT NOME INTO V_NOME_DET FROM DETRITOS WHERE ID_DETRITO = V_CONTADOR;
   DBMS_OUTPUT.PUT_LINE('Bloco 4 (WHILE) - Detrito consultado: ' || V_NOME_DET);
   V_CONTADOR := V_CONTADOR + 1;
 END LOOP;
EXCEPTION 
 WHEN TOO_MANY_ROWS THEN 
    DBMS_OUTPUT.PUT_LINE('Bloco 4 - Erro: Múltiplas linhas.');
 WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE('Bloco 4 - Outro erro capturado.');
END;
/

-- Bloco 5
DECLARE
 V_MENSAGEM VARCHAR2(50) := 'NeoHorizon API Test Log';
BEGIN
 FOR I IN 1..5 LOOP
   PRC_REGISTRAR_CONSULTA(1, V_MENSAGEM || ' - Bloco ' || I);
 END LOOP;
 DBMS_OUTPUT.PUT_LINE('Bloco 5 (FOR) - Consultas injetadas via lote com variáveis.');
EXCEPTION 
 WHEN OTHERS THEN 
    DBMS_OUTPUT.PUT_LINE('Bloco 5 - Falha na inserção.');
END;
/

-- Bloco 6
DECLARE 
 V_IDX NUMBER := 1;
 V_EMP VARCHAR2(100);
BEGIN
 LOOP
   SELECT EMPRESA INTO V_EMP FROM CLIENTES WHERE ID_CLIENTE = V_IDX;
   DBMS_OUTPUT.PUT_LINE('Bloco 6 (LOOP-EXIT) - Cliente: ' || V_EMP);
   V_IDX := V_IDX + 1;
   EXIT WHEN V_IDX > 3;
 END LOOP;
EXCEPTION 
 WHEN NO_DATA_FOUND THEN 
    DBMS_OUTPUT.PUT_LINE('Bloco 6 - ID não localizado.');
END;
/

